const lines = rawText.split('\n');
const questions = [];
let currentQ = null;

const formatLetter = (str) => {
    let clean = str.replace(/[^a-zA-Z0-9]/g, '');
    if (clean.toLowerCase() === 'true') return 'A';
    if (clean.toLowerCase() === 'false') return 'B';
    return clean.toUpperCase();
}

for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    if (!line) continue;

    // Check if new question
    const qMatch = line.match(/^(\d+)\s*\.\s*(.*)/);
    if (qMatch) {
        if (currentQ) questions.push(currentQ);
        currentQ = {
            type: "mcq",
            question: qMatch[2],
            options: [],
            answer: ""
        };
        continue;
    }

    // Check if continuation of question (no a), b), or Ans: )
    if (currentQ && !line.match(/^[a-eA-E]\s*[.)]/) && !line.match(/^[a-eA-E]\s+-/) && !line.match(/^(?:Ans|ANSWER):?/i)) {
         currentQ.question += " " + line;
         continue;
    }

    // Check if answer FIRST
    const ansMatch = line.match(/^(?:Ans|ANSWER)\s*:?\s*(.*)/i);
    if (ansMatch && currentQ) {
         let ansLetter = formatLetter(ansMatch[1]);
         // Find matching option
         let matchedOpt = currentQ.options.find(o => o.startsWith(ansLetter + "."));
         if (matchedOpt) {
             currentQ.answer = matchedOpt;
         } else {
             currentQ.answer = ansMatch[1] + " (Warning: Not Found)";
         }
         continue;
    }

    // Check if option
    const optMatch = line.match(/^([a-eA-E])\s*[.)-]\s*(.*)/);
    if (optMatch && currentQ) {
         currentQ.options.push(optMatch[1].toUpperCase() + ". " + optMatch[2]);
         continue;
    }
    
    // True/False edge cases:
    if (line.toLowerCase() === 'a) true' || line.toLowerCase() === 'a)true') {
         currentQ.options.push("A. True");
         continue;
    }
    if (line.toLowerCase() === 'b) false' || line.toLowerCase() === 'b)false') {
         currentQ.options.push("B. False");
         continue;
    }
}
if (currentQ) questions.push(currentQ);

fs.writeFileSync('./src/data/sqm_qa.json', JSON.stringify(questions, null, 2));
console.log('Processed', questions.length, 'questions');
